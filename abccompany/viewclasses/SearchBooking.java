    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String hID, cID, sDate, eDate;
            int cc; 
            pst = con.prepareStatement("SELECT * FROM book");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            cc = rsmd.getColumnCount();
            DefaultTableModel dft = (DefaultTableModel) tabal.getModel();
            dft.setRowCount(0);
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
            while (rs.next()) {
                Vector vt = new Vector();
                hID = rs.getString("HallID");
                //cID = rs.getString("CusNIC");
                sDate = rs.getString("StartDate");
                eDate = rs.getString("EndDate");
                if (hallId.getText().equals(hID) && sdf.format(
                        startDate.getDate()).equals(sDate)
                        || sdf.format(endDate.getDate()).equals(
                                eDate)) {
                    vt.add(hallId.getText());
                    switch (hallId.getText()) {
                        case "0001":
                            vt.add(1000);
                            break;
                        case "0002":
                            vt.add(300);
                            break;
                        case "0003":
                            vt.add(500);
                            break;
                    }
                    vt.add("Unavailable");
                    dft.addRow(vt);
                    hallId.setText("");
                    break;
                } else {
                    vt.add(hallId.getText());
                    switch (hallId.getText()) {
                        case "0001":
                            vt.add(1000);
                            break;
                        case "0002":
                            vt.add(300);
                            break;
                        case "0003":
                            vt.add(500);
                            break;
                    }
                    vt.add("Available");
                    dft.addRow(vt);
                    hallId.setText("");
                    break;
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(SearchBooking.class.getName()).log(
                    Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

